#include <stdio.h>
#include <stdlib.h>
#include "3-calc.h"
/**
 * op_add - addition
 * @a: a
 * @b: b
 * Return: operation
 */
int op_add(int a, int b)
{
return (a + b);
}
/**
 * op_sub - addition
 * @a: a
 * @b: b
 * Return: operation
 */
int op_sub(int a, int b)
{
return (a - b);
}
/**
 * op_mul - addition
 * @a: a
 * @b: b
 * Return: operation
 */
int op_mul(int a, int b)
{
return (a * b);
}
/**
 * op_div - addition
 * @a: a
 * @b: b
 * Return: operation
 */
int op_div(int a, int b)
{
return (a / b);
}
/**
 * op_mod - addition
 * @a: a
 * @b: b
 * Return: operation
 */
int op_mod(int a, int b)
{
return (a % b);
}
