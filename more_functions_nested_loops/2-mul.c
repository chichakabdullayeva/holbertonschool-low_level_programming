#include <stdio.h>
#include "main.h"

/**
 * mul - Multiplies two integers.
 * @a: The first integer.
 * @b: The second integer.
 *
 * Description: This function takes two integers as input and
 * returns their product.
 *
 * Return: The product of a and b.
 */
int mul(int a, int b)
{
    return (a * b);
}

